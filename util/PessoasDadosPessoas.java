package util;

public class PessoasDadosPessoas {

    private double altura;
    private char genero;

    public PessoasDadosPessoas(double altura, char genero) {

        this.altura = altura;
        this.genero = genero;
    }

    public double getAltura() {
        return altura;
    }

    public char getGenero() {
        return genero;
    }
}
