package util;

public class PessoasProblemaAlturas {

    private String name;
    private int idade;

    public PessoasProblemaAlturas(String name, int idade) {
        this.name = name;
        this.idade = idade;
    }

    public String getName() {
        return name;
    }

    public int getIdade() {
        return idade;
    }
}
